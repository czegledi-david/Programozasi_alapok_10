#Aritmetikai operátorok (+, -, *, /, %)

#Összeadás:
print(1 + 5) #Az 1 és az 5 az operandus, a + jel az operátor. Az eredmény: 6
#Kivonás:
print(10 - 4) #Eredmény: 6
#Szorzás:
print(2 * 10) #Eredmény: 20
#Osztás:
print(10 / 5) #Eredmény: 2
#Maradékos osztás. Itt az eredmény a maradék lesz:
print(16 % 3) #Eredmény: 1
